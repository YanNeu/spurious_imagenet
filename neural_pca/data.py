def imagenet_resize(image):
    """
    Resizes image to imagenet resolution.
    Arguments:
        image - ndarray, input image
    Returns:
        224x224 PIL image, resized version 
    """    
    return T.CenterCrop(T.Resize(256)(Image.fromarray(image)))


